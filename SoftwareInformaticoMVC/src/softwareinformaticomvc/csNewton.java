public class csNewton {
    private int iteracion;
    private double xi,fxi,fdxi,xni,error, resta;

    public int getIteracion() {
        return iteracion;
    }

    public void setIteracion(int iteracion) {
        this.iteracion = iteracion;
    }

    public double getXi() {
        return xi;
    }

    public void setXi(double xi) {
        this.xi = xi;
    }

    public double getFxi() {
        return fxi;
    }

    public void setFxi(double fxi) {
        this.fxi = fxi;
    }

    public double getFdxi() {
        return fdxi;
    }

    public void setFdxi(double fdxi) {
        this.fdxi = fdxi;
    }

    public double getXni() {
        return xni;
    }

    public void setXni(double xni) {
        this.xni = xni;
    }

    public double getError() {
        return error;
    }

    public void setError(double error) {
        this.error = error;
    }
    public double getResta() {
        return resta;
    }

    public void setResta(double res) {
        this.resta = res;
    }
    
    
    
    
}
